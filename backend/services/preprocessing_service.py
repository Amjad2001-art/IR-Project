# نستورد مكتبة التعبيرات المنتظمة لتنظيف النصوص وإزالة الرموز والروابط.
import re
# نستورد مكتبة معالجة اللغة الطبيعية لاستخدام كلمات التوقف.
import nltk
# نستورد قائمة كلمات التوقف الإنجليزية مثل كلمات الربط الشائعة.
from nltk.corpus import stopwords
# نستورد خوارزمية الجذر المستخدمة لتوحيد أشكال الكلمات.
from nltk.stem import PorterStemmer


# نحمل قائمة كلمات التوقف حتى تكون متاحة عند تشغيل المشروع.
nltk.download("stopwords")

# نحول كلمات التوقف إلى مجموعة حتى يكون البحث فيها سريعاً أثناء المعالجة.
stop_words = set(stopwords.words("english"))
# ننشئ كائن الجذر مرة واحدة ليستخدم في معالجة الوثائق والاستعلامات.
stemmer = PorterStemmer()


# Data Pre-Processing Normalization
# هنا نطبق التطبيع النصي قبل الفهرسة والبحث.
def normalize_text(text):
    # نحول أي قيمة إلى نص ثم نحول الأحرف إلى صغيرة لتوحيد شكل الكلمات.
    text = str(text).lower()
    # نزيل روابط المواقع لأنها لا تفيد في مطابقة محتوى الوثيقة.
    text = re.sub(r"http\S+|www\S+", " ", text)
    # نزيل كل شيء غير الأحرف الإنجليزية والمسافات لتقليل الضجيج.
    text = re.sub(r"[^a-z\s]", " ", text)
    # نوحد المسافات المتكررة ونحذف الفراغات من بداية ونهاية النص.
    text = re.sub(r"\s+", " ", text).strip()
    # نعيد النص بعد التطبيع ليستخدم في باقي مراحل المعالجة.
    return text


# Stemming
# هنا نحذف كلمات التوقف  ونطبق الجذر لتوحيد أشكال الكلمات.
def preprocess_stemming(text):
    # نبدأ بتطبيق التطبيع حتى تدخل كل النصوص بنفس الشكل.
    text = normalize_text(text)
    # نقسم النص إلى كلمات منفصلة.
    words = text.split()

    # نخزن هنا الكلمات النهائية بعد التنظيف والجذر.
    tokens = []

    # نمر على كل كلمة في النص.
    for word in words:
        # نحذف كلمات التوقف والكلمات القصيرة جداً لأنها غالباً غير مفيدة.
        if word not in stop_words and len(word) > 2:
            # نطبق الجذر ثم نضيف الكلمة الناتجة إلى القائمة النهائية.
            tokens.append(stemmer.stem(word))

    # نعيد الكلمات المعالجة كنص واحد حتى تستخدمه النماذج والفهارس.
    return " ".join(tokens)


# Query Processing
# هنا نعالج استعلام المستخدم بنفس منطق معالجة الوثائق.
def process_query(query):
    # نطبق المعالجة المسبقة على نص الاستعلام.
    processed_query = preprocess_stemming(query)
    # نقسم الاستعلام المعالج إلى كلمات حتى يمكن عرضها أو استخدامها.
    query_tokens = processed_query.split()

    # نرجع الاستعلام الأصلي والمعالج والكلمات الناتجة للتوضيح والاختبار.
    return {
        # الاستعلام كما كتبه المستخدم.
        "original_query": query,
        # الاستعلام بعد التطبيع وحذف كلمات التوقف وتطبيق الجذر.
        "processed_query": processed_query,
        # قائمة الكلمات النهائية الناتجة من المعالجة.
        "tokens": query_tokens
    }
